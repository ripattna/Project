def add(x, y):
    """Adding Function"""
    return x + y


def subtract(x, y):
    """Subtract FUnction"""
    return x - y


def multiply(x, y):
    """Multiply Function"""
    return x * y


def divide(x, y):
    """Divide Function"""
    if y == 0:
        raise ValueError('Can not derived by zero!.')
    return x / y




